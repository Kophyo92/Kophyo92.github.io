#include<iostream>
using namespace std;
int main(){
    int a,b;
    a = 4;
    b = 2;

    //cout<<"a + b = "<<(a + b);// with or without ()in a + b is not important, just to see clearly
    cout<<a<<" + "<<b<<" = "<<(a + b)<<"\n";
    cout<<a<<" - "<<b<<" = "<<(a - b)<<"\n";
    cout<<a<<" * "<<b<<" = "<<(a * b)<<"\n";
    cout<<a<<" / "<<b<<" = "<<(a / b)<<"\n";
    cout<<a<<" % "<<b<<" = "<<(a % b)<<"\n";
    return 0;
}
